package com.csfe.trs.business.entity;

public class Vesvoyforwarder {

	public String sono;
	public String vesvoyno;
	public String carrierNo;
	public String voyage;
	
	public Vesvoyforwarder (){
		super();
	}

	public String getSono() {
		return sono;
	}

	public void setSono(String sono) {
		this.sono = sono;
	}

	public String getVesvoyno() {
		return vesvoyno;
	}

	public void setVesvoyno(String vesvoyno) {
		this.vesvoyno = vesvoyno;
	}

	public String getCarrierNo() {
		return carrierNo;
	}

	public void setCarrierNo(String carrierNo) {
		this.carrierNo = carrierNo;
	}

	public String getVoyage() {
		return voyage;
	}

	public void setVoyage(String voyage) {
		this.voyage = voyage;
	}

	@Override
	public String toString() {
		return "Vesvoyforwarder [sono=" + sono + ", vesvoyno=" + vesvoyno + ", carrierNo=" + carrierNo + ", voyage="
				+ voyage + "]";
	}
}
