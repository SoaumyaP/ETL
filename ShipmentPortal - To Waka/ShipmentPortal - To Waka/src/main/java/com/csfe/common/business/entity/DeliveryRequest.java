package com.csfe.common.business.entity;

import java.util.Date;

public class DeliveryRequest {
	
	private long reqID;
	private String systemID;
	private String reqType;
    private String processID;
    private String emailFrom;
    private String emailTo;
    private String emailCC;
    private String emailBCC;
    private String emailSubject;
    private String emailContent;
    private String remarks;    
    private String status;
    private String createdBy;	
    private Date createdOn;
    private String updatedBy;
	private Date updatedOn;
	
	public DeliveryRequest() {
		super();
	}

	public long getReqID() {
		return reqID;
	}

	public void setReqID(long reqID) {
		this.reqID = reqID;
	}

	public String getSystemID() {
		return systemID;
	}

	public void setSystemID(String systemID) {
		this.systemID = systemID;
	}

	public String getReqType() {
		return reqType;
	}

	public void setReqType(String reqType) {
		this.reqType = reqType;
	}

	public String getProcessID() {
		return processID;
	}

	public void setProcessID(String processID) {
		this.processID = processID;
	}

	public String getEmailFrom() {
		return emailFrom;
	}

	public void setEmailFrom(String emailFrom) {
		this.emailFrom = emailFrom;
	}

	public String getEmailTo() {
		return emailTo;
	}

	public void setEmailTo(String emailTo) {
		this.emailTo = emailTo;
	}

	public String getEmailCC() {
		return emailCC;
	}

	public void setEmailCC(String emailCC) {
		this.emailCC = emailCC;
	}

	public String getEmailBCC() {
		return emailBCC;
	}

	public void setEmailBCC(String emailBCC) {
		this.emailBCC = emailBCC;
	}

	public String getEmailSubject() {
		return emailSubject;
	}

	public void setEmailSubject(String emailSubject) {
		this.emailSubject = emailSubject;
	}

	public String getEmailContent() {
		return emailContent;
	}

	public void setEmailContent(String emailContent) {
		this.emailContent = emailContent;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

	public Date getCreatedOn() {
		return createdOn;
	}

	public void setCreatedOn(Date createdOn) {
		this.createdOn = createdOn;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

	public Date getUpdatedOn() {
		return updatedOn;
	}

	public void setUpdatedOn(Date updatedOn) {
		this.updatedOn = updatedOn;
	}
	
	public DeliveryRequest clone() {
		DeliveryRequest retReq = new DeliveryRequest();
		
		retReq.setSystemID(this.getSystemID());
		retReq.setProcessID(this.getProcessID());
		retReq.setStatus(this.getStatus());
		retReq.setReqType(this.getReqType());
		
		retReq.setEmailFrom(this.getEmailFrom());
		retReq.setEmailTo(this.getEmailTo());
		retReq.setEmailCC(this.getEmailCC());
		retReq.setEmailBCC(this.getEmailBCC());
		retReq.setEmailSubject(this.getEmailSubject());
		retReq.setEmailContent(this.getEmailContent());
		retReq.setRemarks(this.getRemarks());
		retReq.setCreatedBy(this.getCreatedBy());
		retReq.setUpdatedBy(this.getUpdatedBy());

		return retReq;
	}
}
